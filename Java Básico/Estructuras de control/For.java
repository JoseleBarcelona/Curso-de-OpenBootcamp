package CursoJavaBasico.EstructurasControl.Repeticion;

public class For {
    public static void main(String[] args) {

        for (int i = 0; i <10; i++){
            System.out.println("El valor de i es: " + i );

        }
        for (int k = 0; k < 10; k+=2){
            System.out.println("El valor de j se incrementa en 2: " + k );
        }



    }
}
